"""
What is a hash table?

It's like an array but with named indices instead of numbers.
In Python we call them dictionaries (dict).
"""

# regular array - access by index number
arr = ["apple", "banana", "cherry"]
print(arr[0])  # apple

# hash table - access by name
prices = {"apple": 1.50, "banana": 0.75, "cherry": 2.00}
print(prices["apple"])  # 1.50

# way more readable!
